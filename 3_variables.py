#Strings: Text and Numbers

First_name = "Hamza"
print(First_name)
First_name = "Esha"
print(First_name)
First_name = "00786"
print(First_name)

#Integers: Whole Number

Age = 24
print(Age)
Income = 61000
print(Income)
Growth = -35
print(Growth)

#Float: Decimal Numbers

Height = 175.6
print(Height)

Price = 235.30
print(Price)

Distance = 1255.44
print(Distance)

scientific_number = 233.e12
print(scientific_number)

#Boolean: True or False

isAlive = True
print(isAlive)
married = False
print(married)