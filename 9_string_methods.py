#String Methods
strng = "Hamza is the best Hacker, Python Developer and Generative AI Engineer"


#lower()
print(strng.lower())


#casefold()
print(strng.casefold())

#islower()
print(strng.islower())

#upper()
print(strng.upper())

#isupper()
print(strng.isupper())

#count()
print(strng.count('p'))

#length()
print(len(strng))

#endswith()
print(strng.endswith('d'))

#startswith()
print(strng.startswith('H'))

#replace
reptext = strng.replace('best','BEST')
print(reptext)

#find()
print(strng.find('H'))

#index()
print(strng.index('P'))

#isnumeric()
print(strng.isnumeric())

#title()
print(strng.title())

#strip
txt = "     Hamza     "
print(txt)
print(txt.strip())

#lstrip()
print(txt.lstrip())

#rstrip()
print(txt.rstrip())


