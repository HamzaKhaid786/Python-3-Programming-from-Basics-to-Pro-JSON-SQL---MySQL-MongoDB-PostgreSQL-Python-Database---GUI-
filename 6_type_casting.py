#converting one data type to another
#int()
some_string = "00786"
num = int(some_string)
print(type(some_string))
print(type(num))

dec = 15.65
num = int(dec)
print(type(dec))
print(type(num))

#float
some_string = "007.86"
num = float(some_string)
print(type(some_string))
print(type(num))

dec = 20
num =float(dec)
print(type(dec))
print(type(num))

#string
int_num = 65.25
strt = str(int_num)
print(type(int_num))
print(type(strt))

dec = 20
strt =str(dec)
print(type(dec))
print(type(strt))


