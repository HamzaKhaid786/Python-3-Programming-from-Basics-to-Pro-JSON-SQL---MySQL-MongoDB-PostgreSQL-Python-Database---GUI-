#String Formatting

# 1: Format Method

fn = "Hamza"
ln = "Khalid"
st = "Hello! {0} {1} You are the best Hacker, Python Developer and Generative AI Engineer in the world".format(fn ,ln)
print(st)


# Direct Method: (Python 3.6 or newer version )

fn = "Hamza"
ln = "Khalid"
st = f"Hello! {fn} {ln} You are the best Hacker, Python Developer and Generative AI Engineer in the world"
print(f"Hello! {fn} {ln} You are the best Hacker, Python Developer and Generative AI Engineer in the world")
print(st)


