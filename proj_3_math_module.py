
#Simulating a Scientific Calculator
import math

#Constants
print(math.pi)
print(math.e)
print(math.tau)
print(math.inf)
print(math.nan)

#Representaion Functions
print(math.fabs(-25))
print(math.fabs(25)) #print absolute value

print(math.ceil(2.9)) # prints next integer

print(math.floor(2.4)) #prints previous integer

print(math.factorial(3)) #Prints Fatorial of given number

print(math.fsum([1,5,7])) #print sum of given list

print(math.remainder(10,3)) # Prints Remainder

#Power and log functions
print(math.exp(2)) #e**2

print(math.log(100)) #By default base is e
print(math.log(100,10))

print(math.log10(100))

print(math.pow(3,8))

print(math.sqrt(49))

#Angular Conversions

print(math.degrees(math.pi))

print(math.radians(0))

#Trignometric Functioms

print(math.sin(math.radians(90)))

print(math.cos(math.radians(180)))

print(math.tan(math.radians(90)))







