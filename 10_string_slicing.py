#Slicing
st = "Hamza is the best Hacker, Python Developer and Generative AI Engineer in the World"
print(st)


#Simple Slicing
print(st[6])

#Negative Slicing
print(st[-3])

#positive range slicing
print(st[0:9]) #end index is excluded in this case "9" also start index should be smaller and end should be bigger
print(st[:5])
print(st[9:])
#Negative range slicing
print(st[-13:-6])#end index is excluded in this case "9" also start index should be smaller and end should be bigger
print(st[-13:])
print(st[:-8])
